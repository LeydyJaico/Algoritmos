"""
Eliminar espacios en blanco: Escribe un programa que elimine los espacios en blanco al principio
y al final de una cadena dada por el usuario.

"""
#El strip se usa para eliminar espacios en blanco al principio y al final de una cadena
cadena = input("Introduce una cadena: ")
cadena_limpia = cadena.strip()
#Comparando cadenas
print(cadena)
print(cadena_limpia)